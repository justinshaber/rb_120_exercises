class CircularQueue
  def initialize(max_size)
    @queue = Array.new
    @max_size = max_size
  end

  def enqueue(new_obj)
    queue.shift if queue.size == max_size
    queue << new_obj
  end

  def dequeue
    queue.shift
  end

  private

  attr_reader :queue, :max_size
end

queue = CircularQueue.new(3)
puts queue.dequeue == nil

queue.enqueue(1)
queue.enqueue(2)
puts queue.dequeue == 1

queue.enqueue(3)
